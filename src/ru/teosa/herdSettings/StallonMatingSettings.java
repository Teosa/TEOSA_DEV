package ru.teosa.herdSettings;

public class StallonMatingSettings {

	private int matingQty;
	private int matingPrice;
	
	
	public int getMatingQty() {
		return matingQty;
	}
	public void setMatingQty(int matingQty) {
		this.matingQty = matingQty;
	}
	public int getMatingPrice() {
		return matingPrice;
	}
	public void setMatingPrice(int matingPrice) {
		this.matingPrice = matingPrice;
	}
}
